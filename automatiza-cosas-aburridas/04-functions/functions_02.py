"""
Ejemplo de funciones con parámetros.
"""


def hello_to(name):
    """
    Imprime un saludo con el nombre de la persona.
    Args:
        name (str): el nombre de la persona.
    """
    print("¡Hola, buenos días", name + "!")


hello_to("Agustín")
hello_to("Lorena")
hello_to("Carlitos")
